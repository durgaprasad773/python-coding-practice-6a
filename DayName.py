'''Question: Day Name
In this coding question, you need to write a program that reads a day number and prints the corresponding day name. For example, if the given day number is 1, the day name is Monday; if the given day number is 2, the day name is Tuesday, and so on.'''

a=int(input())

if a==1:
    print("Monday")
elif a==2:
    print("Tuesday")
elif a==3:
    print("Wednesday")
elif a==4:
    print("Thursday")
elif a==5:
    print("Friday")
elif a==6:
    print("Saturday")
elif a==7:
    print("Sunday")